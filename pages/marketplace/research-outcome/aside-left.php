    <script>
        $(function () {
            var filters = $(".search-facets"),
                facetUrl = filters.data('facet-url'),
                showmoretxt = $('#searchfilters').data('show-more-text'),
                showlesstxt = $('#searchfilters').data('show-less-text'),
                showAllFilterOptions = $('#searchfilters').data('list-filter-options'),
                minimumForPopupfilter = 20;

            $(document).ready(function () {
                $(".filter-group").each(function () {
                    fetchFilterData($(this));
                });
            });

            var fetchFilterData = function (filter) {
                var contributorName = filter.data("contributor-name");
                var url = facetUrl + (facetUrl.indexOf('?') != -1 ? '&' : '?') + 'searchFacet=' + contributorName;

                if (url) {
                    $.ajax({
                        type: "GET",
                        url: url
                    }).done(function (data) {
                        if (data) {
                            // PURESUPPORT-47621: remove filters with no real filter available
                            var noOptionsAvailableFilter = !data.includes('class="badges');

                            if (noOptionsAvailableFilter) {
                                filter.remove();
                            } else {

                                $(data).find("div").each(function () {
                                    var familyName = $(this).find('legend').text(),
                                        trimmedFamilyName = $.trim(familyName);

                                    $(filter).append(this);
                                    if ($(this).find("li").length > 5 && $(this).find("li").length <= minimumForPopupfilter) {
                                        $(this).append("<button onclick='showmoreFilter(this);' class='btn-search-filter-show-more' aria-label='" + showmoretxt + ' ' + trimmedFamilyName + ' ' + showAllFilterOptions + "'>" + showmoretxt + "<i class='icon icon-down-open-mini' aria-hidden='true'></i></button>").append("<button onclick='showlessFilter(this);' class='btn-search-filter-show-less' aria-label='" + showlesstxt + ' ' + trimmedFamilyName + ' ' + showAllFilterOptions + "'>" + showlesstxt + "<i class='icon icon-down-open-mini' aria-hidden='true'></i></button>");
                                    }
                                    if ($(this).find("li").length > minimumForPopupfilter) {
                                        $(this).append("<button onclick='showallFilter(this);' class='btn-search-filter-popup' aria-label='" + showmoretxt + ' ' + trimmedFamilyName + ' ' + showAllFilterOptions + "'>" + showmoretxt + "<i class='icon icon-down-open-mini' aria-hidden='true'></i></button>");
                                    }

                                    // Activate link on SPACE since it has role="checkbox"
                                    keyboardActivation($(this));
                                });
                            }
                            alignHeight();
                        }
                    });
                }
            };

            $(document).on('keydown', function (e) {
                if (e.which === $.ui.keyCode.ESCAPE && $("#filter-popup-bgoverlay").length > 0) {
                    closePopup();
                }
            });

            $("#filter-popup").on('keydown', function (e) {
                if (e.which === $.ui.keyCode.ESCAPE) {
                    closePopup();
                }
                else if (e.shiftKey && e.which === $.ui.keyCode.TAB) {
                    if ($(document.activeElement).is('.close-filter')) {
                        e.preventDefault();
                        $('#filter-popup').find('li').last().find('a').focus();
                    }
                }
                else if (e.which === $.ui.keyCode.TAB) {
                    if ($(document.activeElement).is('a') && $(document.activeElement).parent('li').next().length <= 0) {
                        $("#filter-popup .close-filter").focus();
                        return false;
                    }
                }
            });
        });

        function ariaLiveMessage(btn) {
            var filterListName = $(btn).closest('.filter-group').find('legend').text(),
                filterStatus = $(btn).hasClass('btn-search-filter-show-more') ? $('#searchfilters').attr('data-list-expanded-text') : $('#searchfilters').attr('data-list-collapsed-text');
            addToAriaLive("assertive", filterListName + filterStatus);
        }

        function showmoreFilter(btn) {
            $(btn).prev("fieldset").find("li:hidden").show();
            $(btn).hide();
            $(btn).next('.btn-search-filter-show-less').show();
            $(btn).next('button').focus();
            ariaLiveMessage(btn);
            alignHeight();
        }

        function showlessFilter(btn) {
            $(btn).prev().prev("fieldset").find("li:nth-child(n+6)").hide();
            $(btn).hide();
            $(btn).prev('.btn-search-filter-show-more').show();
            $(btn).prev('button').focus();
            ariaLiveMessage(btn);
            alignHeight();

        }

        function showallFilter(btn) {
            $(btn).attr('data-remember-focus', '');
            $(btn).parent(".filter-wrap").clone().addClass("popup").appendTo($("#filter-popup"));
            $("#filter-popup-bgoverlay").show();
            $("#filter-popup").show();
            $("#filter-popup li:first-child a").focus();

            keyboardActivation("#filter-popup");
        }

        function keyboardActivation(container) {
            $(container).find('a').on('keypress', function (e) {
                if (e.which === $.ui.keyCode.SPACE) {
                    window.location = $(this).attr('href');
                    e.preventDefault();
                }
            });
        }

        function closePopupKey(e) {
            if (e.which === $.ui.keyCode.TAB) {
                return false;
                if (e.shiftKey) {
                    $('#filter-popup li:last-child a').focus();
                    return false;
                } else {
                    $('#filter-popup li:first-child a').focus();
                }
            } else if (e.which === $.ui.keyCode.ENTER || e.which === $.ui.keyCode.SPACE) {
                closePopup();
            }
        }

        function closePopup() {
            $("#filter-popup-bgoverlay").hide();
            $("#filter-popup .filter-wrap").remove();
            $("#filter-popup").hide();
            $("[data-remember-focus]").focus().removeAttr('data-remember-focus');
            addToAriaLive("assertive", "Filter popup closed");
        }

        function alignHeight() {
            var height = 0;
            height = $("#searchfilters").height() + (($(".page-section-content-persons-index").length > 0) ? 115 : 30);
            $(".search-filters").closest(".container").css("min-height", height);
        }
